'use strict';

/*
Вывести в консоль таблицу степеней от 1 до 10
*/

console.log('Tаблица степеней от 1 до 10');

for (let i = 1; i <= 10; i++) {
  console.log('-------------------------------');
  
  for (let j = 1; j <= 10; j++) {
    console.log(`${i}**${j} = ${i**j}`);
  }
}
console.log('-------------------------------');
console.log('Конец таблицы степеней');